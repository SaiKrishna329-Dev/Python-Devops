num1 = 15
num2 = 5

result0 = num1 + num2
print("The sum of", num1, "and", num2, "is:", result0)

# Integer Division
result1 = num1 // num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-7)
print("Absolute Value:", result3)